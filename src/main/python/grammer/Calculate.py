# 加法
add = 1 + 3
print(add)

# 减法
minus = add - 2
print(minus)

# 乘法
multi = minus * 4
print(multi)

# 除法-小数
divFloat = multi / 4
print(divFloat)

# 除法-整数
divInt = multi // 4
print(divInt)

# 求面积
width = 3
height = 4
S = width * height
print(S)
